const express = require("express");
const walletController = require("../controllers/walletControllers");
const auth = require("../middleware/auth");
const router = express.Router();

/////////////////////////////////////////////
///////// CREATE WALLET /////////////////////
///////////////////////////////////////////

router.route("/").post(auth, walletController.createWallet);

/////////////////////////////////////////////
///////// GET ALL WALLETS ///////////////////
///////////////////////////////////////////

router.route("/").get(auth, walletController.getAllWallets);

/////////////////////////////////////////////
///////// GET SINGLE WALLET DETAIL /////////////////
///////////////////////////////////////////

router.route("/single/:id").get(auth, walletController.getSingleWallet);

/////////////////////////////////////////////
///////// GET SINGLE User WALLET /////////////////
///////////////////////////////////////////

router.route("/user-wallet-info").get(auth, walletController.getUserWallet);

/////////////////////////////////////////////
///////// UPDATE WALLET /////////////////
///////////////////////////////////////////

router.route("/:id").put(auth, walletController.updateWallet);

module.exports = router;
