const express = require("express");
const resultController = require("../controllers/resultControllers");
const auth = require("../middleware/auth");
const router = express.Router();

/////////////////////////////////////////////
///////// CREATE Result /////////////////////
///////////////////////////////////////////

router.route("/").post(auth, resultController.createResult);

/////////////////////////////////////////////
///////// GET ALL Result ///////////////////
///////////////////////////////////////////

router.route("/").get(auth, resultController.getAllResult);

/////////////////////////////////////////////
///////// GET SINGLE Result /////////////////
///////////////////////////////////////////

router.route("/single/:id").get(auth, resultController.getSingleResult);

/////////////////////////////////////////////
///////// GET SINGLE User Result /////////////////
///////////////////////////////////////////

router.route("/list").get(auth, resultController.getUserResults);

/////////////////////////////////////////////
///////// Remove Quiz /////////////////
///////////////////////////////////////////

router.route("/:id").delete(auth, resultController.removeResult);

module.exports = router;
