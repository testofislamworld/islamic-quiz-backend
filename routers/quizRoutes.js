const express = require("express");
const quizController = require("../controllers/quizControllers");
const auth = require("../middleware/auth");
const router = express.Router();

const multer = require("multer");

// Set up multer storage
const storage = multer.memoryStorage();
const upload = multer({ storage: storage });

/////////////////////////////////////////////
///////// CREATE Quiz /////////////////////
///////////////////////////////////////////

router.route("/").post(auth, quizController.createQuiz);


/////////////////////////////////////////////
///////// UPDATE Quiz /////////////////////
///////////////////////////////////////////
router.route("/:id").put(auth, quizController.updateQuiz);

/////////////////////////////////////////////
///////// CREATE Quiz /////////////////////
///////////////////////////////////////////
router.route("/upload-image").post(auth, upload.single("file"), quizController.addImageToQuiz);

/////////////////////////////////////////////
///////// GET ALL Quiz ///////////////////
///////////////////////////////////////////

router.route("/").get(auth, quizController.getAllQuiz);

/////////////////////////////////////////////
///////// GET SINGLE Quiz /////////////////
///////////////////////////////////////////

router.route("/single/:id").get(auth, quizController.getSingleQuiz);

/////////////////////////////////////////////
///////// GET all Quiz of Category  /////////////////
///////////////////////////////////////////

router
  .route("/category/:category_id")
  .get(auth, quizController.getAllCategoryQuiz);

////////////////////////////////////////
